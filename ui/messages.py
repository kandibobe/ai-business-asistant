def format_document_list(documents: List[Dict[str, Any]], page: int = 1, per_page: int = 5) -> str:
    """–°–ø–∏—Å–æ–∫ –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤"""
    if not documents:
        return """
üì≠ <b>–£ –≤–∞—Å –ø–æ–∫–∞ –Ω–µ—Ç –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤</b>

–ó–∞–≥—Ä—É–∑–∏—Ç–µ –≤–∞—à –ø–µ—Ä–≤—ã–π –¥–æ–∫—É–º–µ–Ω—Ç:
‚Ä¢ üìÑ PDF —Ñ–∞–π–ª
‚Ä¢ üìä Excel —Ç–∞–±–ª–∏—Ü—É
‚Ä¢ üìù Word –¥–æ–∫—É–º–µ–Ω—Ç
‚Ä¢ üåê URL –≤–µ–±-—Å—Ç—Ä–∞–Ω–∏—Ü—ã
‚Ä¢ üé§ –ê—É–¥–∏–æ –∑–∞–ø–∏—Å—å

–ò–ª–∏ –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–æ–º–∞–Ω–¥—É /help –¥–ª—è –ø–æ–¥—Ä–æ–±–Ω–æ–π –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏.
"""

    total = len(documents)
    start = (page - 1) * per_page
    end = start + per_page
    page_docs = documents[start:end]

    result = f"üìö <b>–í–∞—à–∏ –¥–æ–∫—É–º–µ–Ω—Ç—ã</b> (–í—Å–µ–≥–æ: {total})\n\n"

    for idx, doc in enumerate(page_docs, start=start+1):
        icon = {'pdf': 'üìÑ', 'excel': 'üìä', 'word': 'üìù', 'url': 'üåê', 'audio': 'üé§'}.get(
            doc.get('type', '').lower(), 'üìé'
        )
        active = " ‚úÖ" if doc.get('is_active') else ""
        result += f"{idx}. {icon} <b>{doc.get('name', '–ë–µ–∑ –Ω–∞–∑–≤–∞–Ω–∏—è')}</b>{active}\n"
        result += f"   üìÖ {doc.get('created_at', 'N/A')} | üí¨ {doc.get('questions_count', 0)} –≤–æ–ø—Ä–æ—Å–æ–≤\n\n"

    if total > per_page:
        result += f"\nüìÑ –°—Ç—Ä–∞–Ω–∏—Ü–∞ {page} –∏–∑ {(total + per_page - 1) // per_page}"

    return result

